package com.xiangxun.atms.framework.compnents.fusioncharts.nodebean;

public class Connector {
	private String strength;
	private String label;
	private String from;
	private String to;
	private String color;
	private String arrowAtStart;
	private String arrowAtEnd;
	/**
	 * @return the strength
	 */
	public String getStrength() {
		return strength;
	}
	/**
	 * @param strength the strength to set
	 */
	public void setStrength(String strength) {
		this.strength = strength;
	}
	/**
	 * @return the label
	 */
	public String getLabel() {
		return label;
	}
	/**
	 * @param label the label to set
	 */
	public void setLabel(String label) {
		this.label = label;
	}
	/**
	 * @return the from
	 */
	public String getFrom() {
		return from;
	}
	/**
	 * @param from the from to set
	 */
	public void setFrom(String from) {
		this.from = from;
	}
	/**
	 * @return the to
	 */
	public String getTo() {
		return to;
	}
	/**
	 * @param to the to to set
	 */
	public void setTo(String to) {
		this.to = to;
	}
	/**
	 * @return the color
	 */
	public String getColor() {
		return color;
	}
	/**
	 * @param color the color to set
	 */
	public void setColor(String color) {
		this.color = color;
	}
	/**
	 * @return the arrowAtStart
	 */
	public String getArrowAtStart() {
		return arrowAtStart;
	}
	/**
	 * @param arrowAtStart the arrowAtStart to set
	 */
	public void setArrowAtStart(String arrowAtStart) {
		this.arrowAtStart = arrowAtStart;
	}
	/**
	 * @return the arrowAtEnd
	 */
	public String getArrowAtEnd() {
		return arrowAtEnd;
	}
	/**
	 * @param arrowAtEnd the arrowAtEnd to set
	 */
	public void setArrowAtEnd(String arrowAtEnd) {
		this.arrowAtEnd = arrowAtEnd;
	}
	
	
}
